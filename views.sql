# Database Specialist - Projetos SQL

Este repositório contém a solução para dois grandes desafios da formação:

### 🚀 Performance e Lógica (Módulo 1)
- Índices para otimização de consultas no esquema 'Company'.
- Stored Procedures para automação de processos.

### 🛡️ Segurança e Automação (Módulo 2)
- Views para abstração de relatórios.
- Triggers para manutenção de logs e estoque.
- DCL (Data Control Language) para gestão de usuários e permissões.
